function replaceIconsInNode(node) {
  if (!node) return;
  
  // Find all i elements with class matching fontawesome
  const iconElements = node.querySelectorAll ? node.querySelectorAll('i') : [];
  iconElements.forEach(i => {
    let matched = false;
    for (const className of i.classList) {
      if (SVG_ICONS[className]) {
        // Replace <i> element with SVG element
        const parser = new DOMParser();
        const doc = parser.parseFromString(SVG_ICONS[className], 'image/svg+xml');
        const svgEl = doc.documentElement;
        
        // Preserve any custom inline styles from the original <i> element
        if (i.getAttribute('style')) {
          svgEl.setAttribute('style', i.getAttribute('style'));
        }
        if (i.id) {
          svgEl.id = i.id;
        }
        
        // Add all non-FA classes to the SVG to preserve custom styling selectors
        i.classList.forEach(cls => {
          if (!cls.startsWith('fa') && cls !== 'fas' && cls !== 'far') {
            svgEl.classList.add(cls);
          }
        });

        i.parentNode.replaceChild(svgEl, i);
        matched = true;
        break;
      }
    }
    
    // Fallback support for partial match / unregistered icons using Unicode / standard indicators
    if (!matched && (i.classList.contains('fas') || i.classList.contains('fa') || i.classList.contains('far'))) {
      const classStr = Array.from(i.classList).join(' ');
      if (classStr.includes('spin') || classStr.includes('spinner')) {
        i.outerHTML = SVG_ICONS['fa-spinner'];
      }
    }
  });
}

// Initial replacements
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => replaceIconsInNode(document.body));
} else {
  replaceIconsInNode(document.body);
}

// Set up MutationObserver to automatically swap new dynamic icons (like in ajax/tables)
const observer = new MutationObserver((mutations) => {
  mutations.forEach(mutation => {
    mutation.addedNodes.forEach(node => {
      if (node.nodeType === Node.ELEMENT_NODE) {
        replaceIconsInNode(node);
      }
    });
  });
});
observer.observe(document.body, { childList: true, subtree: true });

// Export global function just in case
window.replaceIconsManual = () => replaceIconsInNode(document.body);
